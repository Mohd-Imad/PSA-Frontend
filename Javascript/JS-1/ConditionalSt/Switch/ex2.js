var letter = 'B';

switch (letter) {
    case 'A':
        console.log('Yes A is a vowel');
        break;
    case 'E':
        console.log('Yes E is a vowel');
        break;
    case 'I':
        console.log('Yes I is a vowel');
        break;
    case 'O':
        console.log('Yes O is a vowel');
        break;
    case 'U':
        console.log('Yes U is a vowel');
        break;
    default:
        console.log('No, the given letter is  not a vowel');
        break;
}