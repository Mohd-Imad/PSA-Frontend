//String indexing

let firstName= "harshit";

//  h   a   r   s   h   i   t     --string
//  0   1   2   3   4   5   6     --index values

console.log(firstName);           //printing complete string 
console.log(firstName[0]);        //printing specific character using string indexing
console.log(firstName[1]);
console.log(firstName[2]);
console.log(firstName[3]);
console.log(firstName[4]);
console.log(firstName[5]);
console.log(firstName[6]);
console.log(firstName.length);                   //printing the length of the string 
console.log(firstName[firstName.length-1]);      //for printing last character of the string
console.log(firstName[firstName.length-2]);      //for printing sceond last character of the string
