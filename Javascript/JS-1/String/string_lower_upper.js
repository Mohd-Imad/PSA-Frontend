// toUpperCase()-- a method which converts all the letters of a string tp uppercase
// toLowerCase()-- a method which converts all the letters of a string tp lowercase

let fName="RaHuL   "
console.log(fName);            
console.log(fName.length);        //printing length

fName=fName.trim();               //performing trim() method to a variable
console.log(fName.length);        //printing length after trimming
console.log(fName.toUpperCase());        //printing string after converting to uppercase
console.log(fName.toLowerCase());        //printing string after converting to lowercase
