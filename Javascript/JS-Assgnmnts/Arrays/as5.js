//Q. how to create Array of Objects in JS?

//Ans. Array of objects is created by array name followed by Object inside the square brackets[], and object properties should be placed inside flower brackets{}

//Ex:
let employees = [{ id: 101, name: 'Rahul' },         // creating Array of Objects       //here, id & name are object properties
{ id: 102, name: "Sonia" },
{ id: 103, name: "Priyanka" }]
console.log(employees);                              //reading Array of Objects
console.log(employees.length)                        //arr.length  
