//Q. how to access items from Arrays in JS?

//Ans. It is accessed by referring to the index value of array element in square brackets []

//Ex:
let a = [10, 20, 30, 40]
//indexing 0  1   2   3

//how to read? using array name
console.log(a)

//how to access array values ? using indexing 
console.log(a[0])
console.log(a[1])
console.log(a[2])
console.log(a[3])
console.log(a[10])